package proyecto.modelo;

public class Articulo {
    
private int Co_Articulo;
private String No_Articulo;
private String UM;

    public int getCo_Articulo() {
        return Co_Articulo;
    }

    public void setCo_Articulo(int Co_Articulo) {
        this.Co_Articulo = Co_Articulo;
    }

    public String getNo_Articulo() {
        return No_Articulo;
    }

    public void setNo_Articulo(String No_Articulo) {
        this.No_Articulo = No_Articulo;
    }

    public String getUM() {
        return UM;
    }

    public void setUM(String UM) {
        this.UM = UM;
    }
     
}
