public class Resultado {

    private String res1;
    private String res2;
public Resultado(){
}
public Resultado(String res1, String res2) {
this.res1 = res1;
this.res2 = res2;
}
public String getRes1() {
return this.res1;
}
public void setRes1(String res1) {
this.res1 = res1;
}
public String getRes2() {
return this.res2;
}
public void setRes2(String res2) {
this.res2 = res2;
}

}
